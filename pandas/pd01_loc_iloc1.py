import pandas as pd

data = [
    ["삼성", '1000', '2000'],
    ["현대", '1100', '3000'],
    ["엘지", '2000', '500'],
    ["아모레", '3500', '6000'],
    ["네이버", '100', '1500'],
]

idx = ['031', '059', '033', '045', '023' ]
col = ['종목명', '시가', '종가']

df = pd.DataFrame(data=data, index=idx, columns=col)

print(df)
print("===================================")
